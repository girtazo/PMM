public class Rectangulo {
    private float ancho;
    private float alto;
    public Rectangulo(float ancho,float alto){
        this.ancho = ancho;
        this.alto = alto;
    }
    public float getAlto(){
        return  this.alto;
    }
    public float getAncho(){
        return  this.ancho;
    }
}
